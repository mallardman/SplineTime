    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed
        
        StringBuilder pitchSwitch = new StringBuilder(jComboBox3.getSelectedItem().toString());
        pitchSwitch.indexOf("/");
        pitchSwitch.delete(pitchSwitch.indexOf("/") - 1, pitchSwitch.length());
        myGear.setPitchDiametric(Double.parseDouble(pitchSwitch.toString()));
        
        //This edits the formatting 
        if(myGear.getPitchDiametric() >= 64){
            safeguardFormat.applyPattern("0.0000");
        }
        
        else{
            safeguardFormat.applyPattern("0.000");
        }
        
        refresh();
    }//GEN-LAST:event_jComboBox3ActionPerformed

    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
        //Turns an internal gear into an external one
        if(myGear instanceof FilletGearInternal){
            myGear = new FilletGearExternal(myGear);
        }
        
        if(myGear instanceof FlatMajorDiameterGearInternal){
            myGear = new FlatMajorDiameterGearExternal(myGear);
        }
        
        if(myGear instanceof FlatSideFitGearInternal){
            myGear = new FlatSideFitGearExternal(myGear);
        }
        
        this.refresh();
    }//GEN-LAST:event_jRadioButton1ActionPerformed

