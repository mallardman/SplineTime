    //Unit Conversion methods
    //For inputting mm
    private double millimetersToInches(double in){
        return in / 25.4;
    }
    
    //For outputting inches
    private double inchesToMillimeters(double in){
        return in * 25.4;
    }
    
    //Use only on the values input through the left.
    private double inputConv(double input){
        if(!isInputInches)
            return millimetersToInches(input);
        
        else
            return input;
    }
    
    //Use only on the values fed to the output on the right.
    private double outputConv(double input){
        
        if(!isOutputInches)
            return inchesToMillimeters(input);
        
        else
            return input;
    }
    
    
    
    //Method to refresh the stuff on the right
    private void refresh(){
        safeguardFormat.setRoundingMode(RoundingMode.DOWN);
        myGear.runCalculations();
        
        if(!Double.isNaN(myGear.getMajorDiameter())){
            if(this.safeDecimal){
                jTextField1.setText(safeguardFormat.format(outputConv(myGear.getMajorDiameter())));
            }
            
            else{
                jTextField1.setText(
                    safeguardFormat.format(outputConv(myGear.getMajorDiameter())));
            }
        }
        
        safeguardFormat.setRoundingMode(RoundingMode.UP);
        if(!Double.isNaN(myGear.getMinorDiameter())){
            if(this.safeDecimal){
                jTextField2.setText(safeguardFormat.format(outputConv(myGear.getMinorDiameter())));
            }
            
            else{
                jTextField2.setText(
                        safeguardFormat.format(outputConv(myGear.getMinorDiameter())));
            }
        }
        
        
        
        if(!Double.isNaN(myGear.getBaseCircleDiameter())){
            jTextField7.setText(safeguardFormat.format(outputConv(myGear.getBaseCircleDiameter())));
        }
        
        else{
            jTextField7.setText("");
        }
        
        if(!Double.isNaN(myGear.getPitchDiameter())){
           jTextField8.setText(safeguardFormat.format(outputConv(myGear.getPitchDiameter())));
        }
        
        else{
            jTextField8.setText("");
        }
        
        if(!Double.isNaN(myGear.getLead())){
           jTextField3.setText(safeguardFormat.format(outputConv(myGear.getLead())));
        }
        
        else{
           jTextField3.setText("");
        }
    }
    
    //Ensures the significence is correct 
